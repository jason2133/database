<?php

echo "<B> ♣ 1부터 30까지 3의 배수의 합  </B><br>";
echo "------------------------------------ <br>";

$a = 3;                 // 초기값 선언
$sum = 0;             // 누적합 변수 선언

while($a <= 30)    // 조건
{
	echo $a." 까지 누적합 ";

	$sum += $a;    // $sum = $sum + $a 와 같음
	$a += 3;          // $a = $a + 3 와 같음

	echo " = {$sum} <br>";
}
echo "----------------------------------- <br>";
echo " ▶ 3의 배수의 합 ..... {$sum} <br> ";
echo "----------------------------------- ";

?>