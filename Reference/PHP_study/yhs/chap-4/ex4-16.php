<?php

echo "<B> ♣ while문을 이용하여 1부터 10까지 홀수의 합  </B><br>";
echo "------------------------------------------------------ <br>";

$a = 1;                 // 초기값 선언
$sum = 0;             // 누적합 변수 선언

while($a <= 10)    // 조건
{
	echo $a." 까지 누적합 ";

	$sum += $a;    // $sum = $sum + $a 와 같음
	$a += 2;          // $a = $a + 2 와 같음

	echo " = {$sum} <br>";
}
echo "------------------------------------------------------ <br>";
echo " ▶ 홀수의 합 ..... {$sum} ";

?>